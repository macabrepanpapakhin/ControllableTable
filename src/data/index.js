function randomStringGenerator(totalLength) {
  const possibleString =
    "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ1234567890~!@#$%^&*()abcdefghijklmnopqrstuvwxyz";
  let returnString = "";
  for (let i = 0; i < totalLength; i++) {
    const index = Math.floor(Math.random() * 70);

    returnString += possibleString.charAt(index);
  }
  return returnString;
}

